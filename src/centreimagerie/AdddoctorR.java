     public void setRecords(){
        DefaultTableModel model = (DefaultTableModel) tabledoctor.getModel();
    try{
    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/centredb","root","");
    PreparedStatement pst = con.prepareStatement("SELECT * FROM doctor");
    ResultSet rs = pst.executeQuery();
    
    while(rs.next())
    {
    String id = rs.getString("ID");
    String nom = rs.getString("F_name");
    String bdate = rs.getString("B_date");
    String genre = rs.getString("Genre");
    String adate = rs.getString("Add_date");
    String spec = rs.getString("Specialty");
    String phn = rs.getString("Phone");
    String add = rs.getString("Address");
    Object[] obj = {id, nom, bdate, genre,spec,adate, phn, add};
    model = (DefaultTableModel)tabledoctor.getModel();
    model.addRow(obj);
    }
    
    } catch (SQLException ex) {
        Logger.getLogger(RegistrationR.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }
     
