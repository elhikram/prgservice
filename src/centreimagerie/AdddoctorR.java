    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // add code:
        String id = txtid.getText().toString();
        String name = txtname.getText();
        String bd = txtbdate.getDateFormatString();
        String gen = combogenre.getSelectedItem().toString();
        String ad = dateadd.getDateFormatString();
        String sp = combospecia.getSelectedItem().toString();
        String ph = txtphone.getText().toString();
        String add = txtadd.getText();
        if(id.equals("")||name.equals("")||bd.equals("")||gen.equals("Choose")||ad.equals("")||sp.equals("Specialty")||ph.equals("")||add.equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Some fields are empty check again", "ERROR",1);
        }
        else {
        Date selectedDate = txtbdate.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate1 = dateFormat.format(selectedDate);
        
         Date selectedDate2 = dateadd.getDate();
        SimpleDateFormat dateFormat2 = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate2 = dateFormat2.format(selectedDate2);
        
        try{
        String sql = "INSERT INTO doctor"
                +"(ID, F_name, B_date, Genre,Add_date, Specialty, Phone, Address)"
                +"VALUES (?,?,?,?,?,?,?,?)";
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/centredb","root","");
        pst = con.prepareStatement(sql);
        pst.setInt(1, Integer.parseInt(txtid.getText()));
        pst.setString(2, txtname.getText());
        pst.setString(3, formattedDate1);
        pst.setString(4, combogenre.getSelectedItem().toString());
        pst.setString(5, formattedDate2);
        pst.setString(6, combospecia.getSelectedItem().toString());
        pst.setInt(7, Integer.parseInt(txtphone.getText()));
        pst.setString(8, txtadd.getText());
        
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "ADD successfully!");
        
        
        
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
        JOptionPane.showMessageDialog(null, ex);
    }
         
      showtable(); 
      txtid.setText("");
        txtname.setText("");
        txtbdate.setDateFormatString("");
        combogenre.setSelectedItem("Choose");
         dateadd.setDateFormatString("");
         combospecia.setSelectedItem("Specialty");
        txtphone.setText("");
        txtadd.setText("");
      
        }
        
    }//GEN-LAST:event_btnaddActionPerformed

