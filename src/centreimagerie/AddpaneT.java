    public void setRecords(){
        DefaultTableModel model = (DefaultTableModel) tablepane.getModel();
    try{
    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/centredb","root","");
    PreparedStatement pst = con.prepareStatement("SELECT * FROM pane");
    ResultSet rs = pst.executeQuery();
    
    while(rs.next())
    {
    String id = rs.getString("Number");
    String nom = rs.getString("Machine_Name");
    String tid = rs.getString("Tech_ID");
    String tname = rs.getString("Tech_Name");
    String pa = rs.getString("Pane");
    String adate = rs.getString("datepane");
    String descr= rs.getString("Description");
    
    Object[] obj = {id, nom,tid,tname,pa, adate, descr};
    model = (DefaultTableModel)tablepane.getModel();
    model.addRow(obj);
    }
    
    } catch (SQLException ex) {
        Logger.getLogger(RegistrationR.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }
     
     public void search(String str){
        DefaultTableModel model = (DefaultTableModel) tablepane.getModel();
    model = (DefaultTableModel) tablepane.getModel();
    TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
    tablepane.setRowSorter(trs);
    trs.setRowFilter(RowFilter.regexFilter(str));
    
    }


