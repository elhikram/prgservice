    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String n= txtn.getText().toString();
        String name = txtmname.getText();
        String id = txtid.getText().toString();
        String tn = txttname.getText();
        String p = pane.getText();
        String date = datepane.getDateFormatString();
        String ds = desc.getText();
        if(n.equals("")||name.equals("")||id.equals("")||tn.equals("")||p.equals("")||date.equals("")||ds.equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Some fields are empty check again", "ERROR",1);
        }
        else{
        Date selectedDate = datepane.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate1 = dateFormat.format(selectedDate);

        try{
            String sql = "INSERT INTO pane"
            +"(Number, Machine_Name, Tech_ID, Tech_Name, Pane, Pane_Date, Description)"
            +"VALUES (?,?,?,?,?,?,?)";
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/centredb","root","");
            pst = con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(txtn.getText()));
            pst.setString(2, txtmname.getText());
            pst.setInt(3, Integer.parseInt(txtid.getText()));
            pst.setString(4, txttname.getText());
            pst.setString(5, pane.getText());
            pst.setString(6, formattedDate1);
            pst.setString(7, desc.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "ADD successfully!");

        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        showtable();
        txtn.setText("");
        txtmname.setText("");
        txtid.setText("");
        txttname.setText("");
        pane.setText("");
       datepane.setDateFormatString("");
       desc.setText("");
        }
    }//GEN-LAST:event_btnaddActionPerformed

